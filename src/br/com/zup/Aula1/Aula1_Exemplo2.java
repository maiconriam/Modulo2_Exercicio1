package br.com.zup.Aula1;

public class Aula1_Exemplo2 {
    public static void main(String[] args) {

        int numero1 = 50;
        int numero2 = 50;

        //Criando operacao de soma
        int resultadoDaSoma = numero1 + numero2;
        //Exibindo a operacao de soma para o usario
        System.out.println(resultadoDaSoma);

        //Criando operacao de subtracao
        int resultadoDaSubtracao= numero1 - numero2;

        //Exibindo a operação de subtracao
        System.out.println(resultadoDaSubtracao);

        //Criando operacao de divisao
        int resultadoDeDivisao = numero1 / numero2;

        //Exibindo a operação de divisão
        System.out.println(resultadoDeDivisao);

        //Criando operacao de multiplicacao
        int resultadoDeMultiplicacao= numero1 * numero2;

        //Exibindo a operacao de multiplicacao
        System.out.print(resultadoDeMultiplicacao);

    }
}
