package br.com.zup.Aula6;

public class Aula6_Exercicio1 {
    public static void main(String[] args) {

        for (int contador = 1; contador <= 100; contador++) {
            System.out.println(contador);
        }

        int contador = 0;

        while (contador <= 100){
            System.out.println(contador);
            contador ++;
        }
    }
}
