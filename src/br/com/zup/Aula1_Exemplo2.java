package br.com.zup;

public class Aula1_Exemplo2 {
    public static void main(String[] args) {

        int numero1 = 50;
        int numero2 = 60;

        //Criando operacao de soma
        int resultadoDaSoma = numero1 + numero2;
        //Exibindo a operacao de soma para o usario
        System.out.println(resultadoDaSoma);

        //Criando operacao de subtracao
        int resultadoDaSubtracao= numero1 - numero2;

        //Exibindo a operação de subtracao
        System.out.print(resultadoDaSubtracao);
    }
}
